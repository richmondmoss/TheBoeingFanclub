            <h2>Boeing 707</h2>
        </div>
    </div>
    <div class="row">
        <div class="span4">
            <img class="img-responsive" src="img/planes/707.jpg">
        </div>
        <div class="span8">
            <p>The Boeing 707 is a mid-sized, long-range, narrow-body, four-engine jet airliner built by Boeing
                Commercial Airplanes from 1958 to 1979. Its name is commonly pronounced as "seven oh seven". Versions of
                the aircraft have a capacity from 140 to 219 passengers and a range of 2,500 to 5,750 nautical miles
                (2,880 to 6,620 mi; 4,630 to 10,650 km).</p>

            <p>Developed as Boeing's first jet airliner, the 707 is a swept-wing design with podded engines. Although it
                was not the first jetliner in service, the 707 was the first to be commercially successful. Dominating
                passenger air transport in the 1960s and remaining common through the 1970s, the 707 is generally
                credited with ushering in the Jet Age. It established Boeing as one of the largest manufacturers
                of passenger aircraft, and led to the later series of airliners with "7x7" designations. The later 720,
                727, 737, and 757 share elements of the 707's fuselage design.</p>
        </div>
