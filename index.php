    <div class="row">
        <div class="span12">
            <h2>Boeing 737</h2>
        </div>
    </div>
    <div class="row">
        <div class="span4">
            <img class="img-responsive" src="img/planes/737.jpg">
        </div>
        <div class="span8">
            <p>The Boeing 737 is a short- to medium-range twinjet narrow-body airliner developed and manufactured by
                Boeing Commercial Airplanes in the United States. Originally developed as a shorter, lower-cost
                twin-engine airliner derived from the 707 and 727, the 737 has developed into a family of ten passenger
                models with capacities from 85 to 215 passengers. The 737 is Boeing's only narrow-body airliner in
                production, with the 737 Next Generation (-700, -800, and -900ER) and the re-engined and redesigned 737
                MAX variants currently being built.</p>

            <p>The 737 was originally envisioned in 1964. The initial 737-100 made its first flight in April 1967, and
                entered airline service in February 1968 at Lufthansa. Next, the lengthened 737-200 entered
                service in April 1968. In the 1980s Boeing launched the longer 737−300, −400, and −500 variants
                (referred to as the Boeing 737 Classic series) featuring CFM56 turbofan engines and wing
                improvements.</p>

            <p>The Boeing 737 Next Generation (NG) was introduced in the 1990s, with a redesigned, increased span
                laminar flow wing, upgraded "glass" cockpit, and new interior.</p>
        </div>
    </div>
    <div class="row">
        <div class="span12">
            <h2>Boeing 757</h2>
        </div>
    </div>
    <div class="row">
        <div class="span8">
            <p>The Boeing 757 is a mid-size, narrow-body twin-engine jet airliner that was designed and built by Boeing
                Commercial Airplanes. It is the manufacturer's largest single-aisle passenger aircraft and was produced
                from 1981 to 2004. The twinjet has a two-crew member glass cockpit, turbofan engines of sufficient power
                to allow takeoffs from relatively short runways and higher altitudes, a conventional tail and, for
                reduced aerodynamic drag, a supercritical wing design. Intended to replace the smaller three-engine 727
                on short and medium routes, the 757 can carry 200 to 295 passengers for a maximum of 3,150 to 4,100
                nautical miles (5,830 to 7,590 km), depending on variant. The 757 was designed concurrently with a
                wide-body twinjet, the 767 and, owing to shared features, pilots can obtain a common type rating that
                allows them to operate both aircraft.</p>

            <p>The 757 was produced in two fuselage lengths. The original 757-200 entered service in 1983; the
                757-200PF, a package freighter (PF) variant, and the 757-200M, a passenger-freighter combi model,
                debuted in the late 1980s. The stretched 757-300, the longest narrow-body twinjet ever produced,
                began service in 1999. Passenger 757-200s have been modified to special freighter (SF) specification for
                cargo use, while military derivatives include the C-32 transport, VIP carriers, and other multi-purpose
                aircraft. Private and government operators have also customized the 757 for research and transport
                roles. All 757s are powered by Rolls-Royce RB211 or Pratt & Whitney PW2000 series turbofans.</p>

