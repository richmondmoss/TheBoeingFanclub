        <div class="span12">
            <h2>Boeing 727</h2>
        </div>
    </div>
    <div class="row">
        <div class="span8">
            <p>The Boeing 727 is a midsized, narrow-body three-engined jet aircraft built by Boeing Commercial Airplanes
                from the early 1960s to 1984. It can carry 149 to 189 passengers and later models can fly up to 2,700
                nautical miles (5,000 km) nonstop. Intended for short and medium-length flights, the 727 can use
                relatively short runways at smaller airports. It has three Pratt & Whitney JT8D engines below the
                T-tail, one on each side of the rear fuselage with a center engine that connects through an S-duct to an
                inlet at the base of the fin. The 727 is Boeing's only trijet aircraft.</p>

            <p>The 727 followed the 707, a quad-jet airliner, with which it shares its upper fuselage cross-section and
                cockpit design. The 727-100 first flew in February 1963 and entered service with Eastern Air Lines in
                February 1964; the stretched 727-200 flew in July 1967 and entered service with Northeast Airlines that
                December. The 727 became a mainstay of airlines' domestic route networks and was also used on short- and
                medium-range international routes. Passenger, freighter, and convertible versions of the 727 were
                built.</p>
        </div>
